function currency_list_with_symbol() {
    $currencies       = currencies();
    $currency_symbols = currency_symbol();
    $currency_list    = [];

    foreach ( $currencies as $iso => $currency ) {
        $symbol = isset( $currency_symbols[ $iso ] ) ? $currency_symbols[ $iso ] : $iso;
        $currency_list[ $iso ] = sprintf( '%1$s ( %2$s )', $currency, $symbol );
    }

    return $currency_list;
}

function currencies_dropdown( $selected = '' ) {
    $options    = '';
    $currencies = currency_list_with_symbol();

    foreach ( $currencies as $key => $value ) {
        $select  = ( $key == $selected ) ? ' selected="selected"' : '';
        $options .= sprintf( "<option value='%s'%s> %s</option>\n", $key, $select, $value );
    }

    return $options;
}
